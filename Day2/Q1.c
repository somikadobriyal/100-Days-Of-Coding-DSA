//Write a C program to delete the element at a given 1-based position pos from an array of n integers. Shift remaining elements to the left.

#include <stdio.h>
int main() 
{
    int arr[100];     
    int n , i;             
    int pos;      // position to delete (1-based)

    // taking number of elements
    printf("Enter number of elements: ");
    scanf("%d", &n);

    // taking array elements
    printf("Enter elements:\n");
    for (i = 0; i < n; i++) 
    {
        scanf("%d", &arr[i]);
    }

    // taking position to delete
    printf("Enter position to delete: ");
    scanf("%d", &pos);

    // Shift elements to the left after deletion
    for (i = pos - 1; i < n - 1; i++) 
    {
        arr[i] = arr[i + 1];
    }
    printf("Updated array:\n");
    for (i = 0; i < n - 1; i++) 
    {
        printf("%d ", arr[i]);
    }
    return 0;
}
